/*
    + Adição
    - Subtração
    * Multiplicação
    / Divisão
    % Resto da divisão
    ** Potenciação
*/

 console.log(1 + 1) // Adição 2
 console.log(10 - 7) // Subtração  3
 console.log(2 * 2) // Multiplicação 4
 console.log(11 % 2) // Resto da divisão 1
 console.log(11 / 2) // Divisão 5.5
console.log(2 ** 3) // Potenciação 8